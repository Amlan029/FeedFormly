"use client"
import { zodResolver } from "@hookform/resolvers/zod"
import { useForm } from "react-hook-form"
import * as  z  from "zod"
import Link from "next/link"
import axios, { AxiosError } from "axios"
import { useEffect, useState } from "react"
import {  useDebounceCallback } from 'usehooks-ts'
import toast, { Toaster } from 'react-hot-toast';
import { useRouter } from "next/navigation"
import { signUpSchema } from "@/schemas/signUpSchema"
import { ApiResponse } from "@/types/ApiResponse"
import { Form, FormControl, FormDescription, FormField, FormItem, FormLabel, FormMessage } from "@/components/ui/form"
import { Input } from "@/components/ui/input"
import { Button } from "@/components/ui/button"
import { Loader2} from "lucide-react"



const page = () => {
  const [username, setUsername] = useState('')
  const [usernameMessage, setUsernameMessage] = useState('')
  const [isCheckingUsername, setIsCheckingUsername] = useState(false)
  const [isSubmit, setIsSubmit] = useState(false)
 //useDebounceCallback helps you wait until the user stops typing (or stops triggering something quickly) before running your code — so you don’t spam your API or slow down your app.
  const debounce = useDebounceCallback(setUsername, 300)
  const router = useRouter()

  //zod implementation
  const form = useForm<z.infer<typeof signUpSchema>>({
    resolver: zodResolver(signUpSchema),
    defaultValues: {
      username: '',
      email: '',
      password: ''
    }
  })

  useEffect(()=> {
    const checkUsernameUnique = async() => {
      if(username) {
         setIsCheckingUsername(true)
         setUsernameMessage('')
         try {
          const response = await axios.get(`/api/check-username-unique?username=${username}`)
          setUsernameMessage(response.data.message)
         } catch (error) {
            const axiosError = error as AxiosError<ApiResponse>;
            setUsernameMessage(axiosError.response?.data.message ?? "Error checking username")
         } finally {
            setIsCheckingUsername(false)
         }
      }
    }
    checkUsernameUnique()
  }, [username])
  const onSubmit = async (data: z.infer<typeof signUpSchema>) => {
    setIsSubmit(true)
    try {
      const response = await axios.post<ApiResponse>('/api/signUp', data)
      toast.success('Success')
      router.replace(`/verify/${username}`)
      setIsSubmit(false)
    } catch (error) {
        console.error("Error in Signup of User",error);
        const axiosError = error as AxiosError<ApiResponse>;
        let errorMSG = axiosError.response?.data.message;
        toast.error(errorMSG ?? "An error occurred while signup")
        setIsSubmit(false)
    }
  }
  return (
    <div className="flex justify-center items-center min-h-screen bg-gray-800">
      <div className="w-full max-w-md p-8 space-y-8 bg-white rounded-lg shadow-md">
        <div className="text-center">
          <h1 className="text-4xl font-extrabold tracking-tight lg:text-5xl mb-6">
            Join Feed Formly
          </h1>
          <p className="mb-4">Sign up to start your anonymous adventure</p>
        </div>
        <Form {...form}>
          <form onSubmit={form.handleSubmit(onSubmit)} className="space-y-6">
            <FormField
              name="username"
              control={form.control}
              render={({ field }) => (
                <FormItem>
                  <FormLabel>Username</FormLabel>
                  <FormControl>
                    <Input placeholder="username" {...field} 
                    onChange={(e) => {
                      field.onChange(e)
                      debounce(e.target.value)
                    }}
                    />
                  </FormControl>
                  {isCheckingUsername && <Loader2 className="animate-spin"/>}
                  <p className={`text-sm ${usernameMessage === "Username is available" ? "text-green-500" : "text-red-500"}`}> {usernameMessage}</p>
                  <FormMessage />
                </FormItem>
              )}
            />
            <FormField
              name="email"
              control={form.control}
              render={({ field }) => (
                <FormItem>
                  <FormLabel>Email</FormLabel>
                  <FormControl>
                    <Input placeholder="email" {...field} 
                    
                    />
                  </FormControl>
                  
                  <FormMessage />
                </FormItem>
              )}
            />
            <FormField
              name="password"
              control={form.control}
              render={({ field }) => (
                <FormItem>
                  <FormLabel>Passowrd</FormLabel>
                  <FormControl>
                    <Input
                    type="password" placeholder="password" {...field} 
                    
                    />
                  </FormControl>
                  
                  <FormMessage />
                </FormItem>
              )}
            />
            <Button 
            type="submit"
            disabled={isSubmit}
            >
              {
                isSubmit ? (
                  <>
                  <Loader2 className="mr-2 h-4 w-4 animate-spin"/> Please wait
                  </>
                ) 
                : ('SignUp')
              }
            </Button>
          </form>
        </Form>
        <div className="text-center mt-4">
          <p>
            Already a member?{' '}
            <Link href="/sign-in" className="text-blue-600 hover:text-blue-800">
              Sign In
            </Link>
          </p>
        </div>
      </div>
    </div>
  )
}

export default page